// Como Parametro
function sumarPorcentaje(pNota, pPorcentaje){
    let nota = pNota
    let porcentaje = pPorcentaje
    let resultadoPorcentaje

    resultadoPorcentaje = nota * porcentaje

    return resultadoPorcentaje
}

